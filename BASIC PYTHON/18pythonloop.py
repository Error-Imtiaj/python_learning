###########  While LOOP ###############

i = 2 # i is a variable 
count = 0

# LOOP STRUCTURE 
# Declare a Variable 
# While (condition with the var) : print var

while i <= 100 :
    # print("HELLO") # WHAT THE LOOP WILL PRINT
    count = count + i
    i = i + 2 # How the condition will work
print("END")




